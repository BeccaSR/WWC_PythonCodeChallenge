# Write a program to find the maximum and minimum values in a list.

# initialise a list with integers as the elements
my_list = [3, 5, 58, 7, 12, 45, 12, 6]

# return the maximum value from the list
print(f'The maximum value in the list is {max(my_list)}.')

# return the minimum value from the list
print(f'The minimum value in the list is {min(my_list)}.')